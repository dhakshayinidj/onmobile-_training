package com.abc;

public class Switchcase {

	  public static void main(String[] args)
	  {
		  int expression = 2;
		  
		  switch(expression)
		  {
		  case 1 :
			  System.out.println("Case1");
		  
		  case 2:
			  System.out.print("Case2");
		  
		  case 3:
			  System.out.println("Case3");
		  
		  case 4 :
			  System.out.println("Case4");
			  
		  default :
			  System.out.println("Default case");
		  }
	  }
}
